void motorAndEncoder() {
  HAL_Delay(1000);
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 0);
  HAL_Delay(500);
  HAL_GPIO_TogglePin(AIN1_GPIO_Port, AIN1_Pin);
  HAL_GPIO_TogglePin(AIN2_GPIO_Port, AIN2_Pin);
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 50);
  HAL_Delay(500);

  if (__HAL_TIM_IS_TIM_COUNTING_DOWN(&htim4)) {
    trace_puts("Counting down.");
  } else {
    trace_puts("Counting up.");
  }
  uint16_t count1 = __HAL_TIM_GET_COUNTER(&htim4);
  HAL_Delay(10);
  uint16_t count2 = __HAL_TIM_GET_COUNTER(&htim4);
  trace_printf("Encoder:%d\n", abs(count2 - count1) * 100); // If count2-count1<0, then the timer is counting down

}

