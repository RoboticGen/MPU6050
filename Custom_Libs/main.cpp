int main(void)
{

  /* USER CODE BEGIN 1 */
  uart_bt = fdopen(UART_BT, "r+");
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_I2C1_Init();
  MX_TIM1_Init();
  MX_USART3_UART_Init();
  MX_TIM4_Init();
  MX_USART1_UART_Init();
  MX_TIM2_Init();

  /* USER CODE BEGIN 2 */
  HAL_Delay(200);
  MPU6050 mpu6050 = MPU6050();
  mpu6050.initialize();
  mpu6050.dmpInitialize();
  mpu6050.setDMPEnabled(true);

  // get expected DMP packet size for later comparison
  packetSize = mpu6050.dmpGetFIFOPacketSize();

//  uint8_t deviceID=0;
//  int8_t state = I2Cdev::readByte(0x68, MPU6050_RA_WHO_AM_I, &deviceID,1000);

#ifdef MOTOR
  HAL_GPIO_WritePin(AIN1_GPIO_Port, AIN1_Pin, GPIO_PIN_SET);
  HAL_GPIO_WritePin(AIN2_GPIO_Port, AIN2_Pin, GPIO_PIN_RESET);

  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_4);
  HAL_TIM_Encoder_Start(&htim4,TIM_CHANNEL_ALL);
#endif
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  HAL_UART_Receive_DMA(&huart3, pData, 1);
  while (1) {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
#ifdef MOTOR
    motorAndEncoder();
#endif

    uint8_t mpuIntStatus = mpu6050.getIntStatus();
    if ((mpuIntStatus & 0x10) || fifoCount == 1024) {
      // reset so we can continue cleanly
      mpu6050.resetFIFO();
      trace_puts("FIFO overflow!");

      // otherwise, check for DMP data ready interrupt (this should happen frequently)
    } else if (mpuIntStatus & 0x02) {
      // wait for correct available data length, should be a VERY short wait
      while (fifoCount < packetSize)
        fifoCount = mpu6050.getFIFOCount();

      mpu6050.getFIFOBytes(fifoBuffer, packetSize);

      fifoCount -= packetSize;

#define DEMO
#ifdef DEMO
      // display quaternion values in InvenSense Teapot demo format:
      teapotPacket[2] = fifoBuffer[0];
      teapotPacket[3] = fifoBuffer[1];
      teapotPacket[4] = fifoBuffer[4];
      teapotPacket[5] = fifoBuffer[5];
      teapotPacket[6] = fifoBuffer[8];
      teapotPacket[7] = fifoBuffer[9];
      teapotPacket[8] = fifoBuffer[12];
      teapotPacket[9] = fifoBuffer[13];
      HAL_UART_Transmit(&huart1, teapotPacket, 14, 100);
      teapotPacket[11]++; // packetCount, loops at 0xFF on purpose

#elif defined YawRollPith
      int yaw, pitch, roll;
      mpu6050.dmpGetQuaternion(&q, fifoBuffer);
      mpu6050.dmpGetGravity(&gravity, &q);
      mpu6050.dmpGetYawPitchRoll(ypr, &q, &gravity);
      yaw = (int) (ypr[0] * 180 / M_PI);
      pitch = (int) (ypr[1] * 180 / M_PI);
      roll = (int) (ypr[2] * 180 / M_PI);
      trace_printf("Yaw:%d\tPitch:%d\tRoll:%d\n", yaw, pitch, roll);
#endif
    }/* MPU6050 DMP */

  }
  /* USER CODE END 3 */

}

